set1 = {'tống', 'đăng', 'quang', 1, 2, 3}
set1.add() # thêm một phần tử vào set
set1.clear() # xóa tất cả các phần tử khỏi set
set1.copy()	# trả về một bản sao của set
set1.difference() # trả về một set chứa sự khác biệt giữa hai hoặc nhiều set
set1.difference_update() # loại bỏ các mục trong set này cũng được bao gồm trong một set khác, được chỉ định
set1.discard() # xóa mục đã chỉ định
set1.intersection()	# trả về một set, đó là giao của hai set khác
set1.intersection_update() # xóa các mục trong set này không có trong (các) set khác, được chỉ định
set1.isdisjoint() # Tkiểm tra hai set có giao nhau hay không
set1.issubset()	# kiểm tra xem set khác có chứa set này không
set1.issuperset() # kiểm tra xem set này có chứa set kia không
set1.pop() # xóa một phần tử khỏi set
set1.remove() # xóa phần tử đã chỉ định
set1.symmetric_difference()	# trả về một set có sự khác biệt đối xứng của hai set
set1.symmetric_difference_update() # chèn sự khác biệt đối xứng từ set này và set khác
set1.union() # trả về một set chứa hợp của các set
set1.update() # cập nhật tập hợp với sự kết hợp của set này và các set khác
